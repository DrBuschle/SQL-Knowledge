-- order your data
-- ORDER BY must always the the last clause in a SELECT statement!!!


-- from one column - ascending or descending

SELECT Column_Name
FROM Table_Name
ORDER BY Column_Name ACS/DESC;		# either choose ASC or DESC



-- from several columns - ascending or descending

SELECT Column_Name, Column_Name_2
FROM Table_Name
ORDER BY Column_Name ASC/DESC, Column_Name_2 ASC/DESC;		# either choose ASC or DESC


